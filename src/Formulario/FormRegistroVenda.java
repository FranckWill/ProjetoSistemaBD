    //Método salvar 

    public boolean SalvarRegistroVendas() {

        RegistroVendas resgistro = new RegistroVendas();
        
        resgistro.setStatusComissao(Double.parseDouble(txtComissao.getText()));
        resgistro.setTotalVenda(Double.parseDouble(txtTotalVendas.getText()));
        resgistro.setDataVenda(txtDataVenda.getText());
        resgistro.setPercentComissao(Double.parseDouble(txtPComisao.getText()));
        resgistro.setCodigoVendedor(vendedor.get(combVendedor.getSelectedIndex()));
        resgistro.setCodigoDepartamento(depart.get(combDepart.getSelectedIndex()));
        RegistroVendasDao registroDao = new RegistroVendasDao();
        registroDao.SalvarRegistroVendas(resgistro);

        return true;

    }

    // Método Atualizar 
    public boolean AtualizarRegistroVendas() {

        RegistroVendas registro = new RegistroVendas();

        registro.setCodigoRegistroVendas(Integer.parseInt(txtCod.getText()));
        registro.setStatusComissao(Double.parseDouble(txtComissao.getText()));
        registro.setTotalVenda(Double.parseDouble(txtTotalVendas.getText()));
        registro.setDataVenda(txtDataVenda.getText());
        registro.setPercentComissao(Double.parseDouble(txtPComisao.getText()));
        registro.setCodigoVendedor(vendedor.get(combVendedor.getSelectedIndex()));
        registro.setCodigoDepartamento(depart.get(combDepart.getSelectedIndex()));
        RegistroVendasDao registroDao = new RegistroVendasDao();
        registroDao.AtualizarRegistroVendas(registro);

        return true;

    }
    
    // Método de excluir

    private boolean ExcluirRegistroVendas() {
        // Classe que contém os métodos get e set
        RegistroVendas registro = new RegistroVendas();
        registro.setCodigoRegistroVendas(Integer.parseInt(txtCod.getText()));
        // Classe que contém o método de exclusão 
        RegistroVendasDao dao = new RegistroVendasDao();
        dao.Excluir(registro);

        return true;

    }

    // método Carregar Tabela
    public void CarregarTabela() {

        Vector cabecalho;
        cabecalho = new Vector();
        cabecalho.add("Código");
        cabecalho.add("StatusComissao");
        cabecalho.add("TotalVendas");
        cabecalho.add("DataVenda");
        cabecalho.add("PercentComissao");
        cabecalho.add("Vendedor");
        cabecalho.add("Departamento");

        RegistroVendasDao registroDao = new RegistroVendasDao();
        tbRegistroV.setModel(new DefaultTableModel(registroDao.CarregarRegistroVendasTabela(), cabecalho));
    }
    
    
   
   // método para a devolução de valores
    
    public  boolean preencherjTextCombox (){
        int x = Integer.parseInt(tbRegistroV.getModel().getValueAt(tbRegistroV.getSelectedRow(),0).toString());
        RegistroVendas registro = new RegistroVendas();
         RegistroVendasDao registroDao =new RegistroVendasDao();
         registro=registroDao.PesquisarRegistro(x);
        
        
             
       combVendedor.getModel().setSelectedItem(registro.getCodigoVendedor());
       combDepart.getModel().setSelectedItem(registro.getCodigoDepartamento());
       
        
        txtCod.setText(String.valueOf(registro.getCodigoRegistroVendas()));
        txtComissao.setText(String.valueOf(registro.getStatusComissao()));
        txtTotalVendas.setText(String.valueOf(registro.getTotalVenda()));
        txtDataVenda.setText(String.valueOf(registro.getDataVenda()));
        txtPComisao.setText(String.valueOf(registro.getPercentComissao()));
        
        return  true;
        
    }
    
