    //Método salvar 

    public boolean SalvarVendedor() {

        Vendedor vendedor = new Vendedor();
        vendedor.setNomeVendedor(txtNome.getText());
        vendedor.setStatusVend(txtStatus.getText());
        vendedor.setDataAdmissao(txtData.getText());
        vendedor.setRg(txtRg.getText());
        vendedor.setDepart(depart.get(combDepart.getSelectedIndex()));
        VendedorDao vendedorDao = new VendedorDao();
        vendedorDao.SalvarVendedor(vendedor);

        return true;

    }
    
    // Método Atualizar 
    public boolean AtualizarVendedor() {

        Vendedor vendedor = new Vendedor();

        vendedor.setCodVendedor(Integer.parseInt(txtcod.getText()));
        vendedor.setNomeVendedor(txtNome.getText());
        vendedor.setStatusVend(txtStatus.getText());
        vendedor.setDataAdmissao(txtData.getText());
        vendedor.setRg(txtRg.getText());
        vendedor.setDepart(depart.get(combDepart.getSelectedIndex()));
        VendedorDao vendDao = new VendedorDao();
        vendDao.AtualizarVendedor(vendedor);

        return true;

    }

    // Método de excluir

    private boolean ExcluirVendedor() {
        // Classe que contém os métodos get e set
        Vendedor vendedor = new Vendedor();
        vendedor.setCodVendedor(Integer.parseInt(txtcod.getText()));
        // Classe que contém o método de exclusão 
        VendedorDao dao = new VendedorDao();
        dao.Excluir(vendedor);

        return true;

    }

    // método Carregar Tabela
    public void CarregarTabela() {

        Vector cabecalho;
        cabecalho = new Vector();
        cabecalho.add("CodigoVendedor");
        cabecalho.add("NomeVendedor");
        cabecalho.add("StatusVendedor");
        cabecalho.add("DataAdmissao");
        cabecalho.add("RG");
        cabecalho.add("CodigoDepartamento");

        VendedorDao vendedorDao = new VendedorDao();
        tbVendedor.setModel(new DefaultTableModel(vendedorDao.CarregarVendedorTabela(), cabecalho));
    }
    
    
   
