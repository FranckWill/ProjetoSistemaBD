    public boolean SalvarSetor() {

        Setor setor = new Setor();
        setor.setNomeSetor(txtNome.getText());
        setor.setCodigoDepartamento(departamentos.get(combDepart.getSelectedIndex()));
        SetorDao setorDao = new SetorDao();
        setorDao.SalvarSetor(setor);

        return true;

    }
    
    // Método Atualizar 
    public boolean AtualizarSetor() {

        Setor setor = new Setor();

        setor.setCodigoSetor(Integer.parseInt(txtcod.getText()));
        setor.setNomeSetor(txtNome.getText());
        setor.setCodigoDepartamento(departamentos.get(combDepart.getSelectedIndex()));
        SetorDao setorDao = new SetorDao();
        setorDao.AtualizarSetor(setor);

        return true;

    }
    
     private boolean ExcluirSetor() {
        // Classe que contém os métodos get e set
        Setor setor = new Setor();
        setor.setCodigoSetor(Integer.parseInt(txtcod.getText()));
        // Classe que contém o método de exclusão 
        SetorDao setorDao = new SetorDao();
        setorDao.Excluir(setor);

        return true;

    }
    
    // método Carregar Tabela
    public void CarregarTabela() {

        Vector cabecalho;
        cabecalho = new Vector();
        cabecalho.add("Código");
        cabecalho.add("Nome");
        cabecalho.add("Departamento");

        SetorDao setorDao = new SetorDao();
        tbSetor.setModel(new DefaultTableModel(setorDao.CarregarSetorTabela(), cabecalho));
    }
    
