    public  boolean preencherjTextCombox (){
        int x = Integer.parseInt(tbSetor.getModel().getValueAt(tbSetor.getSelectedRow(),0).toString());
        Setor setor = new Setor();
         SetorDao setorDao =new SetorDao();
         setor=setorDao.PesquisarDepartamento(x);
        
        
             
       combDepart.getModel().setSelectedItem(setor.getCodigoDepartamento());
       
        
        txtcod.setText(String.valueOf(setor.getCodigoSetor()));
        txtNome.setText(String.valueOf(setor.getNomeSetor()));
        
        
        return  true;
        
    }
    
    public  boolean preencherInformacao (){
        int row = tbSetor.getSelectedRow();
        if (tbSetor.getSelectedRow()!=-1){
            txtcod.setText(String.valueOf(tbSetor.getValueAt(row,0)));
            txtNome.setText(String.valueOf(tbSetor.getValueAt(row,1)));
            combDepart.addItem(String.valueOf(tbSetor.getModel().getValueAt(tbSetor.getSelectedRow(),2)));
       
        }
        return true;
       
    }
    
    public boolean bloquear() {
        txtcod.setEditable(false);
        txtNome.setEditable(false);
        combDepart.setEditable(false);
        btNovo.setEnabled(true);
        btSalvar.setEnabled(false);
        btExcluir.setEnabled(true);
        btEditar.setEnabled(true);
        btCancelar.setEnabled(false);
        btSair.setEnabled(true);
        return true;
    }
    
    public boolean desbloquear() {
        txtcod.setEditable(false);
        txtNome.setEditable(true);
        combDepart.setEditable(true);
        btNovo.setEnabled(true);
        btSalvar.setEnabled(true);
        btExcluir.setEnabled(true);
        btEditar.setEnabled(false);
        btCancelar.setEnabled(true);
        btSair.setEnabled(true);
        return true;
    }

