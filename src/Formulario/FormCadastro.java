    public boolean SalvarCadastro() {

        Cadastro cadastro = new Cadastro();
        cadastro.setUser(txtUser.getText());
        cadastro.setPass(txtPass.getText());
        CadastroDAO cadastroDao = new CadastroDAO();
        cadastroDao.SalvarCadastro(cadastro);

        return true;

    }

